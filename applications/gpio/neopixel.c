void neopixel_configure() {
    furi_hal_spi_bus_handle_init(&furi_hal_spi_bus_handle_external);
    buffer_length = STRIP_LENGTH * 12 + 2;
    buffer[0] = 0; //clear the preamble byte
    buffer[buffer_length - 1] = 0; // clear the post send cleardown byte
    neopixel_clear();
}

void neopixel_update() {
    furi_hal_spi_acquire(&furi_hal_spi_bus_handle_external);
    furi_hal_spi_bus_tx(&furi_hal_spi_bus_handle_external, buffer, buffer_length, 1000);
    furi_hal_spi_release(&furi_hal_spi_bus_handle_external);
}

void neopixel_set_color(uint8_t index, uint32_t color) {
    uint8_t r, g, b;

    r = (uint8_t)(color >> 16),
    g = (uint8_t)(color >>  8),
    b = (uint8_t)color;

    uint8_t *bptr = buffer + index * 12 + 1;
    uint8_t *tPtr = (uint8_t *)encoderLookup;

    *bptr++ = *tPtr++;
    *bptr++ = *tPtr++;
    *bptr++ = *tPtr++;

    tPtr = (uint8_t *)encoderLookup + g * 3;
    *bptr++ = *tPtr++;
    *bptr++ = *tPtr++;
    *bptr++ = *tPtr++;

    tPtr = (uint8_t *)encoderLookup + r * 3;
    *bptr++ = *tPtr++;
    *bptr++ = *tPtr++;
    *bptr++ = *tPtr++;

    tPtr = (uint8_t *)encoderLookup + b * 3;
    *bptr++ = *tPtr++;
    *bptr++ = *tPtr++;
    *bptr++ = *tPtr++;
}

void neopixel_clear() {
    uint8_t * bptr= buffer+1; // first byte in the buffer is a preable and is always zero. hence the +1
    uint8_t *tPtr;

    for(int i = 0; i < STRIP_LENGTH * 4; i++) {
        tPtr = (uint8_t *)encoderLookup;
        *bptr++ = *tPtr++;
        *bptr++ = *tPtr++;
        *bptr++ = *tPtr++;
    }
}
